    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:

        EmployeeDao employeeDao = new EmployeeDaoImpl();
        DoctorFeeSummaryDao doctorFeeSummaryDao = new DoctorFeeSummaryDaoImpl();

        LocalDate todayLocalDate = LocalDate.now(ZoneId.of("America/Montreal"));
        java.sql.Date sqlDate = java.sql.Date.valueOf(todayLocalDate);
        /*
        DoctorFeeSummary(int id, String doctorName, int doctorId, double totalFee, 
        double paidFee, double dueFee, Employee employee) 
         */

        String doctorName = txtDoctorName.getText().trim();
        int doctorId = Integer.parseInt(txtDoctorId.getText().trim());
        totalFee = Double.parseDouble(txtTotalFee.getText().trim());
        paidFee = Double.parseDouble(txtPaidFee.getText().trim());
        Double dueFee = totalFee - paidFee;
        int empID = Integer.parseInt(cmbEmployee.getSelectedItem().toString());

        /*
        DoctorFeeSummary(int id, String doctorName, int doctorId, double totalFee, 
        double paidFee, double dueFee, Employee employee) 
         */
        Employee e = employeeDao.getEmployeeByEmployeeLastName(doctorName);
        // Medical m = medicalDao.getMedicalByMedicalName(medical.trim());
        DoctorFeeSummary doctorFeeSummary = new DoctorFeeSummary(doctorName, doctorId, totalFee, paidFee, dueFee, e);
               
               

        doctorFeeSummaryDao.save(doctorFeeSummary);

        displayDoctoFeeSumaryListIntoTable(doctorFeeSummary);


    }//GEN-LAST:event_btnAddActionPerformed

