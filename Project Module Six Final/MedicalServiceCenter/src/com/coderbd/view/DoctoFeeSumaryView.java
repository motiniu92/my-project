    public void displayDoctoFeeSumaryListIntoTable(DoctorFeeSummary doctorFeeSummary) {
        //clearTable();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] cols = new Object[5];
        /*
        DoctorFeeSummary(int id, String doctorName, int doctorId, double totalFee, 
        double paidFee, double dueFee, Employee employee) 
         */

        cols[0] = doctorFeeSummary.getDoctorName();
        cols[1] = doctorFeeSummary.getDoctorId();
        cols[2] = doctorFeeSummary.getTotalFee();
        cols[3] = doctorFeeSummary.getPaidFee();
        cols[4] = doctorFeeSummary.getDueFee();

     
        model.addRow(cols);
    }

    public void clearTable() {
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        model.setRowCount(0);
    }

